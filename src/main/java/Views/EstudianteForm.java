    private void jBtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGuardarActionPerformed
        // TODO add your handling code here:
        boolean result = false;
        try {
            APIEstudiantes api = new APIEstudiantes("http://localhost/MVC/Controllers/APIRest.php");
            switch (this.header) {
                case "Editar":
                    setEstudiante(); // llena this.current con los datos del formulario 
                    api.PUT(this.current); // llama PUT con el objeto Estudiante 
                    break;
                case "Agregar":
                    this.current = new Estudiante();
                    setEstudiante();
                    api.POST(this.current); // llama POST con el objeto Estudiante 
                    break;
                default:
                    result = false;
                    break;
            }
            ResponseEstudiante response = new ResponseEstudiante(api);
            result = response.parseSuccess();
        } catch (Exception e) {
            e.printStackTrace();
            result = false;
        }
        this.dispose();
        padre.acualizarTabla();
    }//GEN-LAST:event_jBtnGuardarActionPerformed

