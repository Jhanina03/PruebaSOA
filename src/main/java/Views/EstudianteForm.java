    private void setEditData() {
        if (this.current == null) {
            return;
        }
        this.jTxtCedula.setText(this.current.cedula);
        this.jTxtCedula.setEnabled(false);
        this.jTxtNombre.setText(this.current.nombre);
        this.jTxtApellido.setText(this.current.apellido);
        this.jTxtDireccion.setText(this.current.direccion);
        this.jTxtTelefono.setText(this.current.telefono);
        if (this.header.equals("Ver")) {
            this.jTxtApellido.setEnabled(false);
            this.jTxtDireccion.setEnabled(false);
            this.jTxtNombre.setEnabled(false);
            this.jTxtTelefono.setEnabled(false);
            this.jBtnGuardar.setEnabled(false);
        }
    }

    private void setEstudiante() {
        this.current.cedula = this.jTxtCedula.getText();
        this.current.nombre = this.jTxtNombre.getText();
        this.current.apellido = this.jTxtApellido.getText();
        this.current.direccion = this.jTxtDireccion.getText();
        this.current.telefono = this.jTxtTelefono.getText();
    }

